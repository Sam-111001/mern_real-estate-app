import mongoose from "mongoose";

//Creating a Schema for User
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      require: true,
      unique: true,
    },
    email: {
      type: String,
      require: true,
      unique: true,
    },
    password: {
      type: String,
      require: true,
    },
  },
  { timestamps: true }
);

//Creating a Model for User
const User = mongoose.model("User", userSchema);

export default User;
